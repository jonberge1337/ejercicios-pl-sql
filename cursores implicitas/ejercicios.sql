CREATE OR REPLACE FUNCTION NUMERO_CLI(PRO PROVINCIAS.PROVINCIA%TYPE)
RETURN NUMBER
IS
	CURSOR CONSULTA IS
		   SELECT CLIENTE
		   FROM CLIENTES
		   WHERE EXISTS(SELECT *
		   		 		FROM PROVINCIAS
						WHERE PROVINCIAS.PROVINCIA = CLIENTES.PROVINCIA
						AND PROVINCIAS.PROVINCIA = PRO);

	CLI CLIENTES.CLIENTE%TYPE;
	CANTIDAD NUMBER(8) := 0;
BEGIN
	OPEN CONSULTA;
	LOOP
		FETCH CONSULTA INTO CLI;
		EXIT WHEN CONSULTA%NOTFOUND;		
		CANTIDAD := CANTIDAD + 1;
	END LOOP;
	CLOSE CONSULTA;
	RETURN CANTIDAD;
	
END NUMERO_CLI;
