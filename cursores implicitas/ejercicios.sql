	FETCH CONSULTA INTO EMP;
	WHILE CONSULTA%FOUND LOOP
		  DBMS_OUTPUT.PUT_LINE(EMP);
		  FETCH CONSULTA INTO EMP;
	END LOOP;
	
END VER_CLIENTES;
